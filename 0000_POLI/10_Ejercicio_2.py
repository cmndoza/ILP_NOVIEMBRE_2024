import math

area_circulo = 0
perimetro_circulo = 0
radio = 0
pi = math.pi

float (area_circulo)
float (perimetro_circulo)
radio = float(input("Determine el radio a utilizar = "))

area_circulo = pi * pow (radio,2)
perimetro_circulo = 2 * pi * radio

print ("EL AREA DEL CIRCULO ES = ",area_circulo)
print ("EL PERIMETRO DEL CIRCULO ES = ",perimetro_circulo)